import Foundation
import UIKit

extension UIColor {
    // Theme Color
    class func themeColor(alpha: CGFloat) -> UIColor {
        return UIColor(red: 144.0/255.0, green: 92.0/255.0, blue: 220.0/255.0, alpha: alpha)
    }

}
